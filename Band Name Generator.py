#Day 1 - Activity
#This very simple program is created for 100DaysOfCode challenge for Python
#This program will help people in choosing a band name based on their input

#1. Create a greeting for your program.
print("Hello! Welcome to R's Band Name Generator. \n")

#2. Ask the user for the city that they grew up in.
city = input("Enter the name of the city that you grew up in: ")

#3. Ask the user for the name of a pet.
pet = input("What is the name of your pet? ")

#4. Combine the name of their city and pet and show them their band name.
print(f"Your band name is {city}{pet} \n")